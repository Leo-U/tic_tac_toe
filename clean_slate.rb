def print_board_row (a,b,c, left_sp, left_div, right_div)
  left_sp.each_line.with_index do |line, index|
    puts line.strip + a.lines[index].strip + left_div.lines[index].strip + b.lines[index].strip + right_div.lines[index].strip + c.lines[index].strip
  end
end

def print_board (section_top, top_left_gap, top_left_vert, top_right_vert, upper_horiz, middle_left_gap, middle_left_vert, middle_right_vert, lower_horiz, bottom_left_gap, bottom_left_vert, bottom_right_vert, section_bottom, x1)
  puts section_top
  print_board_row(x1, x1, x1, top_left_gap, top_left_vert, top_right_vert)
  puts upper_horiz
  print_board_row(x1, x1, x1, middle_left_gap, middle_left_vert, middle_right_vert)
  puts lower_horiz
  print_board_row(x1, x1, x1, bottom_left_gap, bottom_left_vert, bottom_right_vert)
  puts section_bottom
end


